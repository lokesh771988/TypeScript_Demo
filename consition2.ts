var week:number=5;

switch(week){
    case 1: console.log("sun"); break;
    case 2: console.log("mon"); break;
    case 3: console.log("tus"); break;
    case 4: console.log("wend"); break;
    case 5: console.log("thus"); break;
    case 6: console.log("fri"); break;
    case 7: console.log("sat"); break;
    default:
        console.log("invalid week");
}